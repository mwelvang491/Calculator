public class AreaCalculator {
    
    
    public double calculateAreaOfRectangle(double length, double width){
        
        
    return length * width;
    }
    
    public double calculateAreaOfCircle(double radius){
        
        
    return Math.PI * (radius * radius);
    }
    
    
    
}
